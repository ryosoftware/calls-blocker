fun phoneCodesForIso(isos: Set<String>): Set<String> =
    countries.filter { it.iso in isos }.map { it.code }.toSet()

fun findCountryByPhoneNumber(phoneNumber: String): Pair<Country, String>? {
    if (!phoneNumber.startsWith("+")) return null
    val digits = phoneNumber.drop(1)
    if (digits.isEmpty()) return null
    for (len in minOf(digits.length, 3) downTo 1) {
        val country = countries.firstOrNull { it.code == digits.take(len) }
        if (country != null) return country to digits.drop(len)
    }
    return null
}

